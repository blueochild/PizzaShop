CREATE TABLE PIZZA(
    PCODE CHAR(4) NOT NULL,
    PNAME VARCHAR2(30),
    COST NUMBER(7),
    CONSTRAINT PK_PIZZA PRIMARY KEY (PCODE)
);

CREATE TABLE SHOP(
    SCODE CHAR(4) NOT NULL,
    SNAME VARCHAR2(20),
    CONSTRAINT PK_SHOP PRIMARY KEY (SCODE)
);

CREATE TABLE SALELIST(
    SALENO NUMBER(7) NOT NULL,
    SCODE CHAR(4) NOT NULL REFERENCES SHOP (SCODE),
    SALEDATE DATE,
    PCODE CHAR(4) NOT NULL REFERENCES PIZZA (PCODE),
    AMOUNT NUMBER(5),
    CONSTRAINT PK_SALELIST PRIMARY KEY (SALENO)
);